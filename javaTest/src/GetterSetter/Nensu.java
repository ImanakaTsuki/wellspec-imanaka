package GetterSetter;


public class Nensu {
	
	private static int nenrei; 
	private static int year;
	private static int umare;
	
	public static int getNenrei() {
		return nenrei;
	}
	public static void setNenrei(int n) {
		nenrei = n;
	}
	public static int getYear() {
		return year;
	}
	public static void setYear(int y) {
		year = y;
	}
	public static int getUmare() {
		return umare;
	}
	public static void setUmare(int u) {
		umare = u;
	}

}
